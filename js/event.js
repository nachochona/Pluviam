const eventosBox = document.getElementById('eventos');

function ShowEvent(event) {
    event.forEach(element => {
        eventosBox.innerHTML += `
        <div class="${element.month} ${element.day} ${element.proximo}">
            <div class="${element.dayL} ${element.nada}">
                <div class="${element.dayN} ${element.nada} ${element.line}">   
                    <p>${element.dia}</p>
                    <p>${element.diaN}</p>
                </div>
                <p>${element.mes}</p>
                <div class="${element.dayP} ${element.nada} ${element.line}">
                    <p>${element.place}<span> ${element.calle}</span></p>
                    <p>${element.ciudad}</p>
                    <p>${element.hora}</p>
                </div>  
            </div>
            <div class="${element.dayR} ${element.nada} ${element.quitar}">
                <a href="${element.link}" target="_blank"><i class="bi bi-${element.youtube}"></i></a>
                <span id="${element.prox}">${element.proxW}</span>
            </div>
        </div>
        
        
        `
    });
}


